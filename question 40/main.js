//Exercise 40 - Albums
function make_album(artist, title) {
    var album = { artist: artist, title: title };
    return album;
}
//creating dictionaries of album
var album1 = make_album('Jungkook', 'Golden');
var album2 = make_album('V Taehyung', 'Layover');
var album3 = make_album('Suga', 'D-Day');
//printing these dictionaries on console
console.log(album1);
console.log(album2);
console.log(album3);
